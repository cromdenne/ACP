                        <h3>WHAT IS PI 26?</h3>
                        <p>Education for Employment was established in 1985 in response to the growing concern over the number of youth who failed to make a successful transition from school to postsecondary endeavors. s. 121.02 (1)(m), Wis. Stats., states that every school board shall provide access to an Education for Employment program. Chapter PI 26, the administrative rule for this program, was revised and became effective on July 1, 2004.  On June 30, 2013, Wisconsin Statute <a class="hvr" href="http://docs.legis.wisconsin.gov/statutes/statutes/115/II/28/59" target="_blank">115.28(59)</a> was signed to require implementation of academic and career planning (ACP) statewide beginning in 2017-18 for pupils enrolled in grades 6 to 12 in a school district.</p>
                        <p>It was determined that rather than creating a new rule addressing ACP, the current PI26 would be updated and revised to include the required ACP components.</p>
